class Calculator
   
    def initialize(num)
     @ini = num.to_i
    end
   

     def add(num1)
      puts  @ini + num1
    
     end

     
      def sub(num2)
     puts @ini - num2
   
      end


      def mul(num3)
       puts @ini * num3
       
      end


      def div(num4)
       puts @ini / num4
       
     end
end


